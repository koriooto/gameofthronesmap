export const SEASON_EPISODES = [10, 10, 10, 10, 10, 10, 7, 6]
export const SEASON_OFFSET = SEASON_EPISODES.reduce(
  (acc, n) => (acc.push(acc[acc.length - 1] + n), acc),
  [0],
)
export const TOTAL_EPISODES = SEASON_OFFSET[SEASON_OFFSET.length - 1] // 73

export const globalEp = (s, e) => SEASON_OFFSET[s - 1] + e
export const epLabel = (idx) => {
  let s = 1
  while (s < 8 && idx > SEASON_OFFSET[s]) s++
  return `Сезон ${s}, серия ${idx - SEASON_OFFSET[s - 1]}`
}

